local M = class("PopupBase", LayoutBase)

---------------------------------------------------------------------------------------------
-- 构造
---------------------------------------------------------------------------------------------
function M:ctor()
	-- 加个遮罩
	self:createMask(0, - self:getIgnoreHeight()/2)
	-- 设个坐标 弹窗坐标去除下面部分
	self:setPosition(self:getCenterPos())
	-- 
	self:ctorCallback()
end

---------------------------------------------------------------------------------------------
-- 移除 mask
---------------------------------------------------------------------------------------------
function M:removeMask()
	self:removeChild(self._mask)
end

---------------------------------------------------------------------------------------------
-- 背景处理函数(派生类可以实现)
---------------------------------------------------------------------------------------------
function M:backgroundClickHandle()
	LayoutManager:back()
end

---------------------------------------------------------------------------------------------
-- 是否全屏
---------------------------------------------------------------------------------------------
function M:isLayout()
	return false
end

return M
